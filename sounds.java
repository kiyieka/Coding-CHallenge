// Coding challenge 1
// This is a simple code to demonstrate class bodies.
// This is a class named sounds that when executed displays
// the sound which a dog makes.

public class sounds { // Declaration of the class.

    void sound(){ // Method declaration
        System.out.println("Bark");
    }
    public static void main(String[] args){ // Declaration of a starter.
        sounds dog = new sounds();
        dog.sound();
    }

}
